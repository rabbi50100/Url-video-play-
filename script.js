function verifyAdComplete() {
    isAdWatched = true;
    clearInterval(adTimerInterval);
    let adBtn = document.getElementById('adBtn');
    adBtn.innerHTML = `<i class="fa-solid fa-check-circle"></i> ${t('adSuccess').split('!')[0]}`;
    adBtn.classList.add('success');
    adBtn.style.background = ""; 
    alert(t('adSuccess'));
}

function watchAd() {
    let adBtn = document.getElementById('adBtn');
    
    if (!isAdClicked) {
        window.open('https://hdmoviewatch.netlify.app/', '_blank'); 
        
        isAdClicked = true;
        adClickTime = Date.now();
        let remainingTime = 10;
        
        adBtn.innerHTML = `<i class="fa-solid fa-spinner fa-spin"></i> ${t('adWaitSpinner')} (${remainingTime}s)`;
        adBtn.style.background = "#1976d2"; 
        
        adTimerInterval = setInterval(() => {
            let elapsed = Math.floor((Date.now() - adClickTime) / 1000);
            remainingTime = 10 - elapsed;
            
            if (remainingTime > 0) {
                adBtn.innerHTML = `<i class="fa-solid fa-spinner fa-spin"></i> ${t('adWaitSpinner')} (${remainingTime}s)`;
            } else {
                clearInterval(adTimerInterval);
                if (document.visibilityState === 'visible') {
                    verifyAdComplete();
                } else {
                    adBtn.innerHTML = `<i class="fa-solid fa-check-double"></i> ${t('adDone')}`;
                    adBtn.style.background = "#f57c00"; 
                }
            }
        }, 1000);
        
    } else {
        let elapsed = Date.now() - adClickTime;
        
        if (elapsed < 10000) {
            alert(t('adIncomplete'));
        } else {
            verifyAdComplete();
        }
    }
}

document.addEventListener("visibilitychange", function() {
    if (document.visibilityState === 'visible' && isAdClicked && !isAdWatched) {
        let elapsed = Date.now() - adClickTime;
        if (elapsed >= 10000) {
            verifyAdComplete();
        }
    }
});

async function setLanguageFromLocation() {
    let savedLang = localStorage.getItem('app_lang');
    if (savedLang && translations[savedLang]) {
        currentLang = savedLang;
        document.getElementById('languageSelect').value = currentLang;
        applyTranslations();
        trackUserLocation();
        return;
    }
    trackUserLocation(true); 
}

async function trackUserLocation(autoSetLang = false) {
    try {
        let response = await fetch('https://ipapi.co/json/');
        let data = await response.json();
        
        if(autoSetLang){
            if (data.country_code === 'BD') {
                currentLang = 'bn';
            } else if (data.country_code === 'IN') {
                currentLang = 'hi';
            } else {
                currentLang = 'en';
            }
            localStorage.setItem('app_lang', currentLang);
            document.getElementById('languageSelect').value = currentLang;
            applyTranslations();
        }

        firebase.database().ref('users_info/' + userDeviceId).update({
            ip: data.ip,
            country: data.country_name,
            country_code: data.country_code,
            language_selected: currentLang,
            last_visited: new Date().toLocaleString()
        });

    } catch (e) {
        if(autoSetLang){
            const browserLang = navigator.language || navigator.userLanguage;
            if (browserLang.toLowerCase().includes('bn')) currentLang = 'bn'; 
            else if (browserLang.toLowerCase().includes('hi')) currentLang = 'hi'; 
            else currentLang = 'en';
            localStorage.setItem('app_lang', currentLang);
            document.getElementById('languageSelect').value = currentLang;
            applyTranslations();
        }
    }
}

