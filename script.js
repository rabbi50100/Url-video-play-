function loadHistoryFromFirebase() {
    const listDiv = document.getElementById('historyList');
    dbRef.on('value', (snapshot) => {
        listDiv.innerHTML = ""; 
        if(!snapshot.exists()){ listDiv.innerHTML = `<p style='text-align:center; color:gray;'>${t('noHist')}</p>`; return; }
        let historyArray =[];
        snapshot.forEach((childSnapshot) => {
            let data = childSnapshot.val();
            data.key = childSnapshot.key; 
            historyArray.push(data);
        });
        historyArray.reverse();

        historyArray.forEach((data) => {
            let iconOrPoster = `<div class="item-play-icon"><i class="fa-solid fa-play"></i></div>`;
            if(data.poster && data.poster !== "") iconOrPoster = `<img src="${data.poster}" class="item-poster" alt="Poster" onerror="this.src='https://via.placeholder.com/50x50?text=No+Poster'">`;
            let safePoster = data.poster ? data.poster.replace(/'/g, "\\'") : "";
            let safeUrl = data.url ? data.url.replace(/'/g, "\\'") : "";

            listDiv.innerHTML += `
                <div class="history-item" onclick="startPlayer('${safeUrl}', '${safePoster}')">
                    ${iconOrPoster}
                    <div class="item-info"><h4>${data.title}</h4><p><small>${data.timestamp}</small></p></div>
                    <div class="history-options" onclick="deleteHistoryItem(event, '${data.key}')"><i class="fa-solid fa-ellipsis-vertical"></i></div>
                </div>`;
        });
    });
}

